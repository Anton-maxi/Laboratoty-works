#Присвойте змінній довільний рядок в та отримайте з неї наступні зрізи, якщо довжина слова достатня для виконання операції зрізу.
#Отримати кожний 3-й символ від 4-го символа з початку і до 20-го символа (20-й символ не включається).

st = str(input("Введіть рядок, який буде перевищувати 20 символів: "))

while len (st) < 20:

    st = str(input("Введіть ще раз рядок, так щоб він перевищував 20 символів: "))
result= st[3:20:3]
print("Після зрізу рядка маємо рядок:",result)